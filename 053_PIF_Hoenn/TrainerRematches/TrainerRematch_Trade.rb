  pbChoosePokemon(1,2,
                  proc {|pokemon|
                    pokemon.hasType?(wanted_type)
                  })

  chosen_index = pbGet(1)
  echoln pbGet(1)
  if chosen_index && chosen_index >= 0
    chosen_pokemon = $Trainer.party[chosen_index]
